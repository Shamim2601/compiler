main ENDP

PRINT PROC
CMP AX, 0
JNL NORMAL 
PUSH AX
MOV DL, 45
MOV AH, 2
INT 21H 
POP AX
NEG AX
NORMAL:
LEA SI, NUMBER_STRING
ADD SI, 5
PRINT_LOOP:
DEC SI
XOR DX, DX
MOV CX, 10
DIV CX
ADD DL, '0'
MOV [SI], DL
CMP AX, 0
JNE PRINT_LOOP
MOV DX, SI
MOV AH, 9
INT 21H
MOV AH, 2	;printing newline
MOV DL, 0DH
INT 21H
MOV DL, 0AH
INT 21H
RET
PRINT ENDP

END MAIN