.MODEL SMALL
.STACK 400H
.DATA
NUMBER_STRING DB '00000$'
t1 DW ?
t2 DW ?
t3 DW ?
t4 DW ?
t5 DW ?
t6 DW ?

.CODE
MOV t1, 0
main PROC
MOV AX,@DATA 
MOV DS,AX

MOV t2, 0
MOV t3, 0
MOV t4, 0
PUSH t4
PUSH 56
POP BX
POP AX
MOV t4, BX
PUSH t1
PUSH 3
POP BX
POP AX
MOV t1, BX

MOV AX, t1
CALL PRINT

MOV AX, t4
CALL PRINT
MOV t5, 0
PUSH t5
PUSH 23
POP BX
POP AX
MOV t5, BX

MOV AX, t5
CALL PRINT
MOV t6, 0
PUSH t6
PUSH 12
POP BX
POP AX
MOV t6, BX

MOV AX, t1
CALL PRINT

MOV AX, t6
CALL PRINT

MOV AH, 4CH 
INT 21H
main ENDP

PRINT PROC
LEA SI, NUMBER_STRING
ADD SI, 5
PRINT_LOOP:
DEC SI
MOV DX, 0
MOV CX, 10
DIV CX
ADD DL, '0'
MOV [SI], DL
CMP AX, 0
JNE PRINT_LOOP
MOV DX, SI
MOV AH, 9
INT 21H
MOV AH, 2	;printing newline
MOV DL, 0DH
INT 21H
MOV DL, 0AH
INT 21H
RET
PRINT ENDP

END MAIN