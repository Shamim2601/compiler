.MODEL SMALL
.STACK 400H
.DATA
NUMBER_STRING DB '00000$'
t0 DB ?

main PROC
MOV AX,@DATA 
MOV DS,AX

MOV BX, 5
MOV AX, BX
CALL PRINT

MOV AH, 4CH 
INT 21H
main ENDP

PRINT PROC
LEA SI, NUMBER_STRING
ADD SI, 5
PRINT_LOOP:
DEC SI
MOV DX, 0
MOV CX, 10
DIV CX
ADD DL, '0'
MOV [SI], DL
CMP AX, 0
JNE PRINT_LOOP
MOV DX, SI
MOV AH, 9
INT 21H
RET
PRINT ENDP

END MAIN